# O tamanho de uma string pode ser obtido utilizando-se a função len. Essa função retorna o número de caracteres na string.

# Exemplo:
len("A")
# saida: 1 # que mostra que o tamanho da string é 1.

# 

len("AB")
# saida: 2 # que mostra que o tamanho da string é 2.

# 

len("")
# saida: 0 # que mostra que o tamanho da string é 0.

# 

len("O rato roeu a roupa")
# saida: 19 # que mostrar que o tamanho da string é 19.

