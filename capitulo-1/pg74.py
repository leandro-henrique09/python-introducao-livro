# Programa 2.2: Cálculo de aumento de salário
salario = 1500 # criamos uma variavel chamada salario que recebe o valor de 1500
aumento = 5 # aqui criamos a variavel aumento que recebe o valor 5
print(salario + (salario * aumento / 100)) # aqui é a fórmula que calculará o valor do novo slário depois de receber um aumento

# O programa 2.2 pode ser escrito de forma mais direta, utilizando outra fórmula sem variáveis(Programa 2.3)

# Programa 2.3: Alternativa para o cáluclo de aumento de salário
